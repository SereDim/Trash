'''
Створіть масив А [1..8] за допомогою генератора випадкових чисел з
елементами від -10 до 10 і виведіть його на екран. Підрахуйте кількість від’ємних
елементів масиву.
'''
from random import randint
c=list()
d=0
for a in range(1,9):#цикл от 1 к 8
    b=randint(-10,10)#рандом от -10 к 10
    if b<0:#подсчёт отрицательных чисел
        d+=1#счётчик
    c.append(b)#переносим в пустой список
print(c)
print(f' {d} - отрицательные числа')
