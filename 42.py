'''
Підрахувати кількість елементів одновимірного масиву, для яких
виконується нерівність i*i<ai<i!
Серебренніков Дмитро
'''
import math
e=0
for a in range(10):#диапазон
    c=math.factorial(a)# факториал итерации
    d=a*a#квадртат итерации
    b=int(input())
    print(f'{c} меньше чем это')#для удобства вывод
    print(f'{d} больше чем это')#
    if d<b<c:#условие
        e+=1#счётчик
print(e)
