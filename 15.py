'''
Знайти суму парних елементів масиву цілих чисел. Розмірність масиву -
20. Заповнення масиву здійснити випадковими числами від 100 до 200.
Серебренніков Дмитро
'''
from random import randint
c=0
for a in range(20):#иапазон чисел
    b=randint(100,200)#массив рандомными числами
    if b%2==0:#условие парности
        c=b+c#рекурсия
print(c)
