'''
Підрахуйте кількість елементів одновимірного масиву, які збігаються зі
своїм номером і при цьому кратні 3.
Серебренніков Дмитро
'''
c=0
for a in range(1,10):#порядок с 1
    b=int(input())
    if b==a and b%3==0:#условие кратности и номеру
        c+=1#щитает шото там
print(c)#выводим количество удовлетворительных условие чисел
