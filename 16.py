'''
Знайти добуток елементів масиву цілих чисел, які кратні 7. Розмірність
масиву - 15. Заповнення масиву здійснити випадковими числами від 10 до 50.
Серебренніков Дмитро
'''
from random import randint
c=1
e=0
for a in range(15):#диапазон
    b=randint(10,50)#массив
    if b%7==0:#условие кратности
        e+=1#чере смотрим есть ли вообще такие числа чтобы не было в конце 1
        c=c*b#произведение
if e==0:
    print(0)
else:
    print(c)
