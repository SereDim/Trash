'''
Дані про температуру повітря за декаду грудня зберігаються в масиві.
Визначити, скільки раз температура була вище середньої за цю декаду.
Серебренніков Дмитро
'''
c=[-10,20,-31]#массив
d=0
e=0
for a in range(len(c)):#цикл
    e=c[a]+e#сумма
f=e//len(c)#среднее арфиметическое
for a in range(len(c)):
    if c[a]>=f:#условие
        d+=1#счётчик
print(d)

    
